/**
 * @brief  The application entry point.
 * @retval int
 */
int main(void) {

	/* USER CODE BEGIN 1 */

	/* USER CODE END 1 */

	/* MCU Configuration--------------------------------------------------------*/

	/* Reset of all peripherals, Initializes the Flash interface and the Systick. */
	HAL_Init();

	/* USER CODE BEGIN Init */

	/* USER CODE END Init */

	/* Configure the system clock */
	SystemClock_Config();

	/* USER CODE BEGIN SysInit */

	/* USER CODE END SysInit */

	/* Initialize all configured peripherals */
	MX_GPIO_Init();
	MX_FATFS_Init();
	MX_USB_DEVICE_Init();
	MX_ADC1_Init();
	MX_TIM4_Init();
	MX_USART2_UART_Init();
	MX_TIM2_Init();
	MX_TIM5_Init();
	/* USER CODE BEGIN 2 */
	// ========================================================================
	// FATFS. Копируем в ОЗУ область флеш-памяти с файловой системой FAT
	memcpy(ramdisk, (uint8_t*) 0x8060000, SECTORS * SECTOR_SIZE);
	FRESULT fr = -1;

	// Запускаем ADC и PWM
	HAL_ADC_Start(&hadc1);
	HAL_TIM_PWM_Start(&htim4, TIM_CHANNEL_1);

	// Устанавливаем ШИМ на 50%
	__HAL_TIM_SET_COMPARE(&htim4, TIM_CHANNEL_1, 500);

	// Монтируем файловую систему из ОЗУ
	fr = f_mount(&USERFatFS, USERPath, 1);
	if (fr != FR_OK) {
		Error_Handler();
	}

	// Инициализируем CSV файл (создаем если нет, очищаем если есть)
	csv_logger_init();

	// ЗАПУСКАЕМ ТАЙМЕР И ADC С ПРЕРЫВАНИЯМИ
	HAL_TIM_Base_Start(&htim2); // Запускаем TIM2 - будет генерировать TRGO каждые 100 мкс
	HAL_ADC_Start_IT(&hadc1); // Запускаем ADC с прерываниями, ждём триггер от TIM2
	HAL_TIM_Base_Start(&htim5); // ЗАПУСКАЕМ ТАЙМЕР ТИМ5
	// ========================================================================
	/* USER CODE END 2 */

	/* Infinite loop */
	/* USER CODE BEGIN WHILE */
	while (1) {
		// ====================================================================
		if (adc_data_ready) {
			uint16_t adc_raw = HAL_ADC_GetValue(&hadc1); // Читаем сырое значение ADC
			uint16_t adc_mv = adc_raw * 3300 / 4095;      // Конвертируем в мВ

			// Вычисляем delta
			static uint32_t previous_timestamp = 0;
			uint32_t delta = 0;
			if (previous_timestamp != 0) {
				delta = adc_timestamp - previous_timestamp;
			}
			previous_timestamp = adc_timestamp;

			csv_logger_write_timestamp(adc_timestamp, adc_mv, delta);

			adc_data_ready = 0;
		}

		if (buffer_index >= 1000) {
			csv_logger_flush();
		}
		// ====================================================================
		/* USER CODE END WHILE */

		/* USER CODE BEGIN 3 */
	}
	/* USER CODE END 3 */
}

